package keeper

import (
	"encoding/json"
	"time"
)

//||------------------------------------------------------------------------------------------------||
//|| Prefix
//||------------------------------------------------------------------------------------------------||

var REDIS_PREFIX = "keeper:"

//||------------------------------------------------------------------------------------------------||
//|| Response
//||------------------------------------------------------------------------------------------------||

func (k KeeperRecord) Save() {
	jsonStr, err := json.Marshal(k)
	if err != nil {
		return
	}
	SessionCache.Set(REDIS_PREFIX+k.KeeperId, jsonStr, time.Hour*24)
}
