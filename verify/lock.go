//||------------------------------------------------------------------------------------------------||
//|| Open: load a Verification from storage by UUID
//||------------------------------------------------------------------------------------------------||

func (v *Verification) Lock() error {
	//||------------------------------------------------------------------------------------------------||
	//|| Fetch Verification JSON
	//||------------------------------------------------------------------------------------------------||
	v.Encrypted = Encrypted{}
	//||------------------------------------------------------------------------------------------------||
	//|| Done
	//||------------------------------------------------------------------------------------------------||
	return nil
}

//||------------------------------------------------------------------------------------------------||
//|| Save: persist a Verification to storage as <uuid>.json
//||------------------------------------------------------------------------------------------------||

func (v *Verification) Unlock() error {
	//||------------------------------------------------------------------------------------------------||
	//|| Serialize
	//||------------------------------------------------------------------------------------------------||
	v.LoadEncrypted()
	//||------------------------------------------------------------------------------------------------||
	//|| Done
	//||------------------------------------------------------------------------------------------------||
	return nil
}
