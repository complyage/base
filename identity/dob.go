package identity

import "github.com/complyage/base/types"

//||------------------------------------------------------------------------------------------------||
//|| UpdateDOB
//||------------------------------------------------------------------------------------------------||

func (i *Identity) UpdateDOB(section string, dob types.DOB, verification string) {
	//||------------------------------------------------------------------------------------------------||
	//|| Iden is top Tier but we got something else
	//||------------------------------------------------------------------------------------------------||
	idRecord := IdentityRecord{
		Verified:     true,
		Age:          dob.Age(),
		DOB:          dob,
		Display:      dob.Mask(),
		Verification: verification,
	}
	//||------------------------------------------------------------------------------------------------||
	//|| Iden is top Tier but we got something else
	//||------------------------------------------------------------------------------------------------||
	switch section {
	case "IDEN":
		i.IDCard = idRecord
	case "FACE":
		i.Face = idRecord
	case "CRCD":
		i.CreditCard = idRecord
	}
	//||------------------------------------------------------------------------------------------------||
	//|| Save
	//||------------------------------------------------------------------------------------------------||
	i.Save()
}
